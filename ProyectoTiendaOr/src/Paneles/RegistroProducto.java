    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        int c=p.busca(leeCodigo());
        if(c!=-1){
            mensaje("Error: Codigo Repetido");
        } else{
            Producto nuevo=new Producto(leeCodigo(),leetipo(),leefoto(),new Informacion(leeMarca(), leeModelo(), leePrecio(), leeDescripcion(),leeTamaño()));
            p.agrega(nuevo);
            lista(nuevo);
            p.graba();
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListaActionPerformed
        // TODO add your handling code here:
        txtSalida.setText("Codigo\tTipo\tMarca\tModelo\tPrecio\tDescripcion\tTamaño\tFoto\n");
        for (int i = 0; i < p.getN(); i++) {
            Producto u = p.getproducto(i);
            imprime(u.getCodigo()+"\t"+u.getTipo()+"\t"+u.getInfo().getMarca()+"\t"+u.getInfo().getModelo()+
                "\t"+u.getInfo().getPrecio()+"\t"+u.getInfo().getDescripcion()+"\t"+u.getInfo().getTamaño()+"\t"+u.getFoto());
        }
    }//GEN-LAST:event_btnListaActionPerformed

