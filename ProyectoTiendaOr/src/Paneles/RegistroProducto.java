    private void lista(Producto p) {
        imprime("Codigo\t:"+p.getCodigo());
        imprime("Tipo\t:"+p.getTipo());
        imprime("Marca\t:"+p.getInfo().getMarca());
        imprime("Modelo\t:"+p.getInfo().getModelo());
        imprime("Precio\t:"+p.getInfo().getPrecio());
        imprime("Descripcion\t:"+p.getInfo().getDescripcion());
        imprime("Foto\t:"+p.getFoto());
        
        lblImagen.setIcon(new ImageIcon(getClass().getResource("/Imagenes/"+leefoto()+".png")));
        
    }
    private void imprime(String s) {
       txtSalida.append(s+"\n");
    }
    private void mensaje(String s) {
        JOptionPane.showMessageDialog(this, s);
    }









