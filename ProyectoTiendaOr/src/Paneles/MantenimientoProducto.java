    public void mostrarLista() {
        iniciaTabla();
        for (int i = 0; i < p.getN(); i++) {
            Producto n = p.getproducto(i);
            Object[] datos = {n.getCodigo(),n.getTipo(),n.getFoto(),
                              n.getInfo().getMarca(),n.getInfo().getModelo(),
                              n.getInfo().getPrecio(),n.getInfo().getDescripcion(),n.getInfo().getTamaño()};
            modelo.addRow(datos);
        }
    }
    public void iniciaTabla() {
        int filas = tabla.getRowCount();
        for (int fila = 0; fila < filas; fila++) {
            modelo.removeRow(0);


        }

        }


