    public void lista(Producto c) {
        cboTipo.setSelectedItem("" + c.getTipo());
        txtMarca.setText("" + c.getInfo().getMarca());
        txtModelo.setText("" + c.getInfo().getModelo());
        txtPrecio.setText("" + c.getInfo().getPrecio());
        txtDescripcion.setText("" + c.getInfo().getDescripcion());
        txtTamaño.setText("" + c.getInfo().getTamaño());
        txtFoto.setText("" + c.getFoto());
        lblImagen.setIcon(new ImageIcon(getClass().getResource("/Imagenes/"+leefoto()+".png")));
        lblImagen.setText("");
    }
    
